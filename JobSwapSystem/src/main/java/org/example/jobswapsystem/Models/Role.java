package org.example.jobswapsystem.Models;

public class Role
{
    private int role_ID;
    private String name;

    public int getRole_ID()
    {
        return role_ID;
    }

    public void setRole_ID(int role_ID)
    {
        this.role_ID = role_ID;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
